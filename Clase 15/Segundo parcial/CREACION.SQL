CREATE TABLE TIPOS_CUENTAS(
	IDTIPO INT NOT NULL PRIMARY KEY,
	DESCRIPCION VARCHAR(50) NOT NULL,
	ESPACIOTOTAL BIGINT NOT NULL,
	COSTO MONEY NOT NULL,
	ESTADO BIT NOT NULL
)
GO
CREATE TABLE CUENTAS(
	IDCUENTA BIGINT NOT NULL PRIMARY KEY,
	NOMBREUSUARIO VARCHAR(50) UNIQUE NOT NULL,
	CLAVE VARCHAR(50) NOT NULL,
	ESPACIO BIGINT NOT NULL,
	IDTIPO INT NOT NULL FOREIGN KEY REFERENCES TIPOS_CUENTAS(IDTIPO),
	IDCUENTAREFERENCIA BIGINT NULL FOREIGN KEY REFERENCES CUENTAS(IDCUENTA),
	ESTADO BIT NOT NULL
)
GO
CREATE TABLE ARCHIVOS(
	IDARCHIVO BIGINT NOT NULL PRIMARY KEY IDENTITY (1, 1),
	IDCUENTA BIGINT NOT NULL FOREIGN KEY REFERENCES CUENTAS (IDCUENTA),
	NOMBRE VARCHAR(100) NOT NULL,
	FECHA SMALLDATETIME NOT NULL,
	TAMAﾃ前 BIGINT NOT NULL,
	ESTADO BIT NOT NULL
)	

